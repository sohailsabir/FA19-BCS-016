class MultiChoice {
  String? questionText;
  String? op1;
  String? op2;
  String? op3;
  String? op4;
  String? answerText;

  MultiChoice(String a, String b, String c, String d, String e, String f) {
    questionText = a;
    op1 = b;
    op2 = c;
    op3 = d;
    op4 = e;
    answerText = f;
  }
}